package itana.news.db;


public class NewsBean {
	private int news_num;
	private String news_sub1;
	private String news_sub2;
	private String news_content;
	private String write_date;
	
	public int getNews_num() {
		return news_num;
	}
	public void setNews_num(int news_num) {
		this.news_num = news_num;
	}
	public String getNews_sub1() {
		return news_sub1;
	}
	public void setNews_sub1(String news_sub1) {
		this.news_sub1 = news_sub1;
	}
	public String getNews_sub2() {
		return news_sub2;
	}
	public void setNews_sub2(String news_sub2) {
		this.news_sub2 = news_sub2;
	}
	public String getNews_content() {
		return news_content;
	}
	public void setNews_content(String news_content) {
		this.news_content = news_content;
	}
	public String getWrite_date() {
		return write_date;
	}
	public void setWrite_date(String write_date) {
		this.write_date = write_date;
	}
	
	
}
